class DayRating {
  final DateTime date;
  final int feelingAsNumber;
  final String feeling;
  final bool annoyed;
  final bool anxious;
  final bool badMental;
  final bool lonely;
  final bool overthinking;
  final bool proud;
  final bool stressed;
  final bool success;
  final bool tired;
  final bool wasGoodDay;

  DayRating(this.date, this.feeling, this.feelingAsNumber, this.annoyed, this.anxious, this.badMental, this.lonely, this.overthinking, this.proud, this.stressed, this.success, this.tired, this.wasGoodDay);
}